package programs;

public class leftpattern {

	public static void main(String arg[])
	{
		int n=5;
		for(int i=1;i<=n;i++)
		{
			for(int m=n;m>i;m--)
			{
				System.out.print(" ");
			}
			for(int k=1;k<=i;k++)
			{
				
			
			System.out.print(n-i+1);
		}
		
		System.out.println();
	}

}
}

